package com.sdgja.utils;

import com.badlogic.gdx.utils.Disposable;

import java.util.ArrayList;

// Utility class - we should add any disposable objects here to make sure we are releasing resources when game ends
final public class DisposableMemoryManager {

    private static ArrayList<Disposable> disposables = new ArrayList<>();

    public static void addDisposable(Disposable disposable) {
        disposables.add(disposable);
    }

    public static void cleanUp() {
        for(Disposable disposable : disposables) {
            disposable.dispose();
        }
        disposables.clear();
    }
}
