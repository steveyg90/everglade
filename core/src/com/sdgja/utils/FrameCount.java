package com.sdgja.utils;

public final class FrameCount {

    private static int frame;

    static {
        frame = 0;
    }

    public static void updateFrameCount() {
        frame++;
        if(frame==0x100)
            frame = 0;
    }

    public static int getFrameCount() {
        return frame;
    }

}
